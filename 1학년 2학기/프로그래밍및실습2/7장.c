#define _CRT_SECURE_NO_WARNINGS
#include <stdio.h>

int main(void)
{
	int n;
	int i = 1;
	printf("출력하고 싶은 단: ");
	scanf("%d", &n);
	while (i <= 9) {
		printf("%d*%d = %d\n", n, i, n * i);
		i++;
	}
	return 0;



	char life = 1;
	int Method = 0;
	while (life == 1) {
		scanf("%d", &Method);
		if (Method == 0) {
			printf("되는걸로 하자.\n");
		}
		else {
			printf("수고했다. 다음거 하자.\n");
		}
	}
	return 0;


	int i, n, sum;

	printf("정수를 입력하시오: ");
	scanf("%d", &n);

	i = 1;
	sum = 0;

	while (i <= n) {
		sum += i;
		i++;
	}
	printf("1부터 %d까지의 합은 %d입니다\n", n, sum);
	return 0;

	int n = 1;
	double money = 0.5;
	while (n <= 30) {
		money *= 2;
		printf("%d일날 현재 금액 = %lf원\n", n, money);
		n++;
	}
}
