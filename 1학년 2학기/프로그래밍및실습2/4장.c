#define _CRT_SECURE_NO_WARNINGS
#include <stdio.h>
#include <limits.h>


int main(void)
{
	int x;
	printf("변수x의 크기: %d\n", sizeof(x));
	printf("char형의 크기: %d\n", sizeof(char));
	printf("int형의 크기: %d\n", sizeof(int));
	printf("short형의 크기: %d\n", sizeof(short));
	printf("long형의 크기: %d\n", sizeof(long));
	printf("float형의 크기: %d\n", sizeof(float));
	printf("double형의 크기: %d\n", sizeof(double));
	return 0;



	short s_money = SHRT_MAX;
	unsigned short u_money = USHRT_MAX;
	s_money += 1;
	printf("s_money = %d\n", s_money);
	u_money = u_money + 1;
	printf("u_money = %d", u_money);
	return 0;



	unsigned int a = -2;
	printf("%d\n", a);
	printf("%u", a);



	char code1 = 'A';
	char code2 = 65;
	printf("code1 = %c\n", code1);
	printf("code2 = %c\n", code2);



	char beep = 7;
	printf("%c", beep);
	char beep = 7;
	printf("%c", beep);



	char code = 'A';
	printf("%d %d %d \n", code, code + 1, code + 2);
	printf("%c %c %c \n", code, code + 1, code + 2);


	
	int x, y, z, sum;
	printf("3rodml wjdtnfmf dlqfurgktpdy (x, y, z): ");
	scanf("%d %d %d", &x, &y, &z);
	sum += x;  // sum을 정의해주지 않아 오류가 발생한다.
	sum += y;
	sum += z;
	printf("3개 정수의 합은 %d입니다.", sum);



	double dis = 149600000.0;
	double l_speed = 300000.0;
	double time;
	time = dis / l_speed;
	time = time / 60.0;
	printf("빛의 속도는 %lfkm/s\n", l_speed);
	printf("태양과 지구와의 거리 %lfkm\n", dis);
	printf("도달 시간은 %lf분", time);
}
